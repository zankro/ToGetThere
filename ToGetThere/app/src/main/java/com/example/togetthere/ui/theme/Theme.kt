@Composable
fun ToGetThereTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    // Dynamic color is available on Android 12+
    dynamicColor: Boolean = true,
    content: @Composable() () -> Unit
) {

    val customTypography = CustomTypography(
        displayLarge = TextStyle(
            fontFamily = abhayalibreFontFamily,
            fontWeight = FontWeight.ExtraBold,
            fontSize = 45.sp,
        ),

        displayLargeWithShadow = TextStyle(
            fontFamily = abhayalibreFontFamily,
            fontWeight = FontWeight.ExtraBold,
            fontSize = 45.sp,
            shadow = Shadow(
                color = MaterialTheme.colorScheme.onSurface,
                offset = Offset(0f, 4f),
                blurRadius = 6f
            )
        ),

        headlineLarge = TextStyle(
            fontFamily = robotoCondensedFontFamily,
            fontWeight = FontWeight.Medium,
            fontSize = 32.sp
        ),

        headlineMedium = TextStyle(
            fontFamily = robotoFlexFontFamily,
            fontWeight = FontWeight.Medium,
            fontSize = 24.sp
        ),

        titleSmall = TextStyle(
            fontFamily = robotoFlexFontFamily,
            fontWeight = FontWeight.ExtraBold,
            fontSize = 16.sp
        ),

        subTitleSmall = TextStyle(
            fontFamily = robotoFlexFontFamily,
            fontWeight = FontWeight.Normal,
            fontSize = 14.sp
        ),

        subTitleSmallBold = TextStyle(
            fontFamily = robotoFlexFontFamily,
            fontWeight = FontWeight.Bold,
            fontSize = 14.sp
        ),

        body = TextStyle(
            fontFamily = robotoFlexFontFamily,
            fontWeight = FontWeight.Normal,
            fontSize = 14.sp
        ),

        bodyMedium = TextStyle(
            fontFamily = robotoFlexFontFamily,
            fontWeight = FontWeight.Medium,
            fontSize = 14.sp
        ),

        bodyBold = TextStyle(
            fontFamily = robotoFlexFontFamily,
            fontWeight = FontWeight.Bold,
            fontSize = 14.sp
        ),

        labelEmphasized = TextStyle(
            fontFamily = robotoSlabFontFamily,
            fontWeight = FontWeight.ExtraBold,
            fontSize = 14.sp
        ),

        label = TextStyle(
            fontFamily = robotoSlabFontFamily,
            fontWeight = FontWeight.Bold,
            fontSize = 14.sp
        ),

        labelVariant = TextStyle(
            fontFamily = robotoFlexFontFamily,
            fontWeight = FontWeight.ExtraBold,
            fontSize = 12.sp
        )
    )

    val colorScheme = when {
//        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
//            val context = LocalContext.current
//            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
//        }
//        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
//            val context = LocalContext.current
//            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
//        }

        darkTheme -> darkScheme
        else -> lightScheme
    }
    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.primary.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = darkTheme
        }
    }


    CompositionLocalProvider(LocalTypography provides customTypography) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = Typography,
            content = content
        )
    }

//    MaterialTheme(
//        colorScheme = colorScheme,
//        typography = Typography,
//        content = content
//    )

}

